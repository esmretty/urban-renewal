def is_target_region(city: str, district: str) -> bool:
    """
    判斷 (city, district) 是否在目標爬取範圍內。
    沒填 district 時只要 city 在 TARGET_REGIONS 就算（用於 early reject 前階段）。
    """
    if not city:
        return False
    if city not in TARGET_REGIONS:
        return False
    if district and district not in TARGET_REGIONS[city]["districts"]:
        return False
    return True


def target_regions_for_frontend() -> dict:
    """給前端下拉選單用的 {city: [district,...]}。"""
    return {c: list(v["districts"].keys()) for c, v in TARGET_REGIONS.items()}

