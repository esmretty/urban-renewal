import socket as _socket

# Windows IPv6 workaround: googleapis IPv6 不通但 getaddrinfo 回傳 IPv6 優先，
# 導致 urllib3/requests 每次首連等 20 秒超時才 fallback IPv4。
# 強制 IPv4 優先排序，徹底消除 Firestore/OAuth2 冷啟動延遲。
_orig_getaddrinfo = _socket.getaddrinfo

def _getaddrinfo_ipv4_first(*args, **kwargs):
    results = _orig_getaddrinfo(*args, **kwargs)
    results.sort(key=lambda x: x[0] != _socket.AF_INET)
    return results

_socket.getaddrinfo = _getaddrinfo_ipv4_first

